function family(name){
    if(name == "Darth Vader"){
        console.log("Luke, I am your father");
    }
    else if(name == "Leia") {
        console.log("Luke, I am your sister");
    }
    else if(name == "Han"){
        console.log("Luke, I am your brother in law");
    }
    else if(name == "R2D2"){
        console.log("Luke, I am your droid")
    }
    else{
        console.log("invalid")
    }
}

family("Han");